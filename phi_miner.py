# Main φ‑Miner Class
# ============================================================================
class PhiMiner:
    def __init__(self, kernel_path: str):
        with open(kernel_path, 'r') as f:
            self.source = f.read()
        self.kernel_name = os.path.splitext(os.path.basename(kernel_path))[0]
        self.params = extract_parameters(self.source)
        print(f"Extracted {len(self.params)} tunable parameters.")
    
    def generate_experiment_grid(self, max_combinations: int = 5000) -> List[Dict[str, Any]]:
        """Generate parameter combinations for experiments."""
        if not self.params:
            return [{}]
        # Use itertools.product, but limit total
        param_names = [p.name for p in self.params]
        param_values = [p.range for p in self.params]
        total = 1
        for r in param_values:
            total *= len(r)
        if total > max_combinations:
            # Subsample using φ‑sparse selection
            grid = []
            for _ in range(max_combinations):
                combo = {}
                for p in self.params:
                    # Choose Fibonacci‑indexed positions
                    idx = random.choice([i for i, _ in enumerate(p.range) if i in FIB or i < len(FIB)])
                    combo[p.name] = p.range[min(idx, len(p.range)-1)]
                grid.append(combo)
            return grid
        else:
            return [dict(zip(param_names, combo)) for combo in itertools.product(*param_values)]
    
    def run_experiments(self, grid: List[Dict]) -> Tuple[List[Dict], List[float]]:
        """Compile, run, and profile each parameter combination."""
        profiler = CUDAProfiler(self.source, self.kernel_name, self.params)
        X = []
        y = []
        print(f"Running {len(grid)} experiments...")
        for i, values in enumerate(grid):
            if i % max(1, len(grid)//10) == 0:
                print(f"  {i}/{len(grid)}")
            metrics = profiler.compile_and_run(values)
            # Use TFLOPS as target for regression
            X.append(values)
            y.append(metrics['TFLOPS'])
        profiler.cleanup()
        return X, y
    
    def mine_formulas(self) -> List[Dict[str, Any]]:
        """Run the full mining pipeline and return discovered formulas."""
        # 1. Generate experiment grid
        grid = self.generate_experiment_grid(max_combinations=1000)
        # 2. Run experiments (or load cached results)
        X, y = self.run_experiments(grid)
        # 3. Symbolic regression
        variables = list(self.params[0].keys()) if self.params else []
        regressor = SymbolicRegressor(variables, population_size=100, generations=10)
        top_exprs = regressor.fit(X, y)
        # 4. Format results
        results = []
        for expr, fitness in top_exprs:
            phi_score_val = phi_score(expr)
            results.append({
                'expression': expr,
                'mse': fitness,
                'phi_score': phi_score_val,
                'interpretation': self._interpret(expr)
            })
        return results
    
    def _interpret(self, expr: str) -> str:
        """Provide a human-readable interpretation of the formula."""
        if 'phi' in expr.lower() and 'occupancy' in expr:
            return "Optimal occupancy involves golden ratio, suggesting a φ‑resonant warp scheduling."
        elif any(str(f) in expr for f in FIB):
            return "Contains Fibonacci numbers, indicating optimal tile sizes or strides."
        return "Formula discovered; further analysis recommended."

