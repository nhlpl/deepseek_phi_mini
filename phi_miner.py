# Symbolic Regression Engine
# ============================================================================
class SymbolicRegressor:
    """
    Genetic programming for symbolic regression.
    Evolves mathematical expressions to fit a dataset.
    """
    def __init__(self, variables: List[str], population_size: int = 200, generations: int = 20):
        self.variables = variables
        self.population_size = population_size
        self.generations = generations
        self.operators = ['+', '-', '*', '/', 'pow', 'log', 'exp', 'sqrt']
        self.constants = [str(c) for c in [0.1, 0.5, 1.0, 2.0, PHI, PHI_INV] + FIB[:5]]
        self.max_depth = 4
    
    def _random_expr(self, depth: int = 0) -> str:
        if depth >= self.max_depth or random.random() < 0.3:
            # Terminal: variable or constant
            return random.choice(self.variables + self.constants)
        op = random.choice(self.operators)
        if op in ['+', '-', '*', '/']:
            left = self._random_expr(depth+1)
            right = self._random_expr(depth+1)
            return f'({left} {op} {right})'
        else:
            arg = self._random_expr(depth+1)
            return f'{op}({arg})'
    
    def _evaluate_expr(self, expr_str: str, data_point: Dict[str, float]) -> float:
        """Safely evaluate an expression with variable bindings."""
        # Replace variable names with values
        eval_str = expr_str
        for var, val in data_point.items():
            eval_str = eval_str.replace(var, str(val))
        # Add math functions
        namespace = {
            'phi': PHI,
            'φ': PHI,
            'pow': math.pow,
            'log': math.log,
            'exp': math.exp,
            'sqrt': math.sqrt,
            'abs': abs,
            'sin': math.sin,
            'cos': math.cos
        }
        try:
            return float(eval(eval_str, {"__builtins__": {}}, namespace))
        except:
            return float('inf')
    
    def fitness(self, expr_str: str, X: List[Dict], y: List[float]) -> float:
        """Mean squared error plus φ‑coherence bonus."""
        errors = []
        for data_point, target in zip(X, y):
            pred = self._evaluate_expr(expr_str, data_point)
            if math.isinf(pred) or math.isnan(pred):
                return float('inf')
            errors.append((pred - target) ** 2)
        mse = np.mean(errors)
        # φ‑coherence bonus
        phi_bonus = phi_score(expr_str)
        # Combine: lower is better
        return mse / (1.0 + 0.1 * phi_bonus)
    
    def mutate(self, expr_str: str) -> str:
        # Simple mutation: replace a subtree
        if random.random() < 0.5:
            return self._random_expr(0)
        # Find a random operator or terminal and replace
        tokens = re.findall(r'[\w\.]+|[()+\\-*/]', expr_str)
        if not tokens:
            return expr_str
        idx = random.randint(0, len(tokens)-1)
        if tokens[idx] in self.operators:
            tokens[idx] = random.choice(self.operators)
        elif tokens[idx] in self.variables + self.constants:
            tokens[idx] = random.choice(self.variables + self.constants)
        return ''.join(tokens)
    
    def crossover(self, expr1: str, expr2: str) -> str:
        # Swap random subtrees (simplified)
        # Find a common operator and swap subtrees
        # For simplicity, just pick one randomly
        return expr1 if random.random() < 0.5 else expr2
    
    def fit(self, X: List[Dict], y: List[float], target_metric: str = 'TFLOPS') -> List[Tuple[str, float]]:
        """Run genetic programming and return top expressions with fitness."""
        population = [self._random_expr() for _ in range(self.population_size)]
        best = []
        for gen in range(self.generations):
            # Evaluate fitness
            fitnesses = [self.fitness(ind, X, y) for ind in population]
            # Keep best
            sorted_pop = sorted(zip(population, fitnesses), key=lambda x: x[1])
            best_in_gen = sorted_pop[0]
            best.append(best_in_gen)
            # Selection (tournament)
            new_pop = [best_in_gen[0]]  # elitism
            while len(new_pop) < self.population_size:
                # Tournament selection
                candidates = random.sample(list(zip(population, fitnesses)), 3)
                parent1 = min(candidates, key=lambda x: x[1])[0]
                candidates = random.sample(list(zip(population, fitnesses)), 3)
                parent2 = min(candidates, key=lambda x: x[1])[0]
                child = self.crossover(parent1, parent2)
                if random.random() < 0.3:
                    child = self.mutate(child)
                new_pop.append(child)
            population = new_pop
        
        # Return top 10 unique expressions
        unique = {}
        for expr, fit in best:
            if expr not in unique or fit < unique[expr]:
                unique[expr] = fit
        return sorted(unique.items(), key=lambda x: x[1])[:10]

